vector<Action> createAllActions()
{
    vector<Action> allActions;
    Action a;
    a.Refueling = 0.0;
    allActions.push_back(a);
    for( int i = a.MinimumAmountRefuel/a.Increment; i <= a.MaximumAmountRefuel/a.Increment; i++ )
    {

        a.Refueling = i*a.Increment;
        allActions.push_back(a);

    }
    return allActions;
}


